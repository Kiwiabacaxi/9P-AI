// ltrInicializar — pesos aleatórios em [-0.5, +0.5] com seed fixa
func ltrInicializar() LtrMLP {
	rng := rand.New(rand.NewSource(42))
	var m LtrMLP
	for i := 0; i < ltrNIn; i++ {
		for j := 0; j < ltrNHid; j++ {
			m.V[i][j] = rng.Float64() - 0.5
		}
	}
	for j := 0; j < ltrNHid; j++ {
		m.V0[j] = rng.Float64() - 0.5
		for k := 0; k < ltrNOut; k++ {
			m.W[j][k] = rng.Float64() - 0.5
		}
	}
	for k := 0; k < ltrNOut; k++ {
		m.W0[k] = rng.Float64() - 0.5
	}
	return m
}
